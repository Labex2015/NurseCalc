public class Hora implements UnidadeMedidaTempoContract {

    static double MICROSEGUNDO = 3600000;
    static double      SEGUNDO = 3600;
    static double      MINUTO  = 60;
    static double      HORA    = 1;
    public static final String LABEL = "Hora";
    public static final String LABELABREVIACAO = "h";

    public double getMicroSegundo(double hora){
        return hora * MICROSEGUNDO;
    }
    public double getSegundo(double hora){
        return hora * SEGUNDO;
    }
    public double getMinuto(double hora){
         return hora * MINUTO;
    }
    public double getHora(double hora){
        return hora * HORA;
    }




}
