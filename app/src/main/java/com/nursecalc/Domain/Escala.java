public class Escala {

    public static final Character NORMAL = 'N';
    public static final Character DIALOG = 'D'; // resultado dividido por 10
    public static final Character INFORMATIVO = 'I'; // exibe PDF

    private String tituloEscala;
    private String descricaoEscala;
    private Character tipo;
    private List<Perguntas> perguntasEscala;
    private List<Analise> analise;

    public List<Analise> getAnalise() {
        return analise;
    }

    public void setAnalise(List<Analise> analise) {
        this.analise = analise;
    }

    public String getTituloEscala() {
        return tituloEscala;
    }

    public void setTituloEscala(String tituloEscala) {
        this.tituloEscala = tituloEscala;
    }

    public String getDescricaoEscala() {
        return descricaoEscala;
    }

    public void setDescricaoEscala(String descricaoEscala) {
        this.descricaoEscala = descricaoEscala;
    }

    public List<Perguntas> getPerguntasEscala() {
        return perguntasEscala;
    }

    public void setPerguntasEscala(List<Perguntas> perguntasEscala) {
        this.perguntasEscala = perguntasEscala;
    }

    public Character getTipo() {
        return tipo;
    }

    public void setTipo(Character tipo) {
        this.tipo = tipo;
    }
}
